package com.example.tryout_kl1.Models;

public class TryoutUser {
    private String idTryoutUser;
    private String idTryout;
    private String idUser;
    private String nilai;

    public TryoutUser() {
    }

    public String getIdTryoutUser() {
        return idTryoutUser;
    }

    public void setIdTryoutUser(String idTryoutUser) {
        this.idTryoutUser = idTryoutUser;
    }

    public String getIdTryout() {
        return idTryout;
    }

    public void setIdTryout(String idTryout) {
        this.idTryout = idTryout;
    }

    public String getIdUser() {
        return idUser;
    }

    public void setIdUser(String idUser) {
        this.idUser = idUser;
    }

    public String getNilai() {
        return nilai;
    }

    public void setNilai(String nilai) {
        this.nilai = nilai;
    }
}
