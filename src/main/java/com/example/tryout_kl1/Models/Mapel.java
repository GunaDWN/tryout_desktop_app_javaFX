package com.example.tryout_kl1.Models;

public class Mapel {
    private String idMapel;
    private String id_tryOut;
    private String namaMapel;
    private int durasiMapel;

    public Mapel() {
    }

    public String getIdMapel() {
        return idMapel;
    }

    public void setIdMapel(String idMapel) {
        this.idMapel = idMapel;
    }

    public String getId_tryOut() {
        return id_tryOut;
    }

    public void setId_tryOut(String id_tryOut) {
        this.id_tryOut = id_tryOut;
    }

    public String getNamaMapel() {
        return namaMapel;
    }

    public void setNamaMapel(String namaMapel) {
        this.namaMapel = namaMapel;
    }

    public int getDurasiMapel() {
        return durasiMapel;
    }

    public void setDurasiMapel(int durasiMapel) {
        this.durasiMapel = durasiMapel;
    }
}
